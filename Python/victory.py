# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –≤–∏–∫—Ç–æ—Ä–∏–Ω–∞: –í—ã–±—Ä–∞—Ç—å –º–∏–Ω–∏–º—É–º 5 –∏–∑–≤–µ—Å—Ç–Ω—ã—Ö –ª—é–¥–µ–π –∏ —É–∑–Ω–∞—Ç—å –∏—Ö –≥–æ–¥ —Ä–æ–∂–¥–µ–Ω–∏—è.
# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –ø–æ –æ—á–µ—Ä–µ–¥–∏ —Å–ø—Ä–∞—à–∏–≤–∞—Ç—å —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –≥–æ–¥ —Ä–æ–∂–¥–µ–Ω–∏—è –∑–Ω–∞–º–µ–Ω–∏—Ç–æ—Å—Ç–∏.
# –ü–æ—Å–ª–µ —Ç–æ–≥–æ –∫–∞–∫ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤–≤–µ–ª –≤—Å–µ –æ—Ç–≤–µ—Ç—ã, –ø—Ä–æ–≥—Ä–∞–º–º–∞ —Å—á–∏—Ç–∞–µ—Ç –∏ –≤—ã–≤–æ–¥–∏—Ç –Ω–∞ —ç–∫—Ä–∞–Ω:
# - –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø—Ä–∞–≤–∏–ª—å–Ω—ã—Ö –æ—Ç–≤–µ—Ç–æ–≤
# - –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –æ—à–∏–±–æ–∫
# - –ø—Ä–æ—Ü–µ–Ω—Ç –ø—Ä–∞–≤–∏–ª—å–Ω—ã—Ö –æ—Ç–≤–µ—Ç–æ–≤ (–º–æ–∂–Ω–æ –ø–æ—Å—á–∏—Ç–∞—Ç—å –∫–∞–∫ –ü–†–ê–í–ò–õ–¨–ù–´–ï –û–¢–í–ï–¢–´*100/–í–°–ï–ì–û –í–û–ü–†–û–°–û–í)
# - –ø—Ä–æ—Ü–µ–Ω—Ç –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ã—Ö –æ—Ç–≤–µ—Ç–æ–≤
# –ü–æ—Å–ª–µ –≤—ã–≤–æ–¥–∞ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –ø—Ä–æ–≥—Ä–∞–º–º–∞ —Å–ø—Ä–∞—à–∏–≤–∞–µ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —Ö–æ—á–µ—Ç –ª–∏ –æ–Ω –Ω–∞—á–∞—Ç—å –∏–≥—Ä—É —Å–Ω–∞—á–∞–ª–∞,
# –µ—Å–ª–∏ –¥–∞ - —Ç–æ –ø–æ–≤—Ç–æ—Ä—è–µ–º –≤—Å–µ —Å–Ω–∞—á–∞–ª–∞, –µ—Å–ª–∏ –æ—Ç–≤–µ—Ç –Ω–µ—Ç - –≤—ã—Ö–æ–¥–∏–º –∏–∑ –ø—Ä–æ–≥—Ä–∞–º–º—ã

people = {'–ê.–°. –ü—É—à–∫–∏–Ω–∞': 1799, '–î–∂–µ–∫–∞ –õ–æ–Ω–¥–æ–Ω–∞': 1876, '–ú.–í. –õ–æ–º–æ–Ω–æ—Å–æ–≤–∞': 1711, '–î.–ò. –ú–µ–Ω–¥–µ–ª–µ–µ–≤–∞': 1834,
          '–°.–í. –ö–æ–≤–∞–ª–µ–≤—Å–∫–æ–π': 1850, '–ò—Å–∞–∞–∫–∞ –ù—å—é—Ç–æ–Ω–∞': 1643, '–ê–ª–∞–Ω–∞ –¢—å—é—Ä–∏–Ω–≥–∞': 1936, '–°.–ü. –ö–æ—Ä–æ–ª–µ–≤–∞': 1907}
yes_no = True


def vic_game():  # –§—É–Ω–∫—Ü–∏—è –ò–≥—Ä—ã –í–∏–∫—Ç–æ—Ä–∏–Ω–∞
    print('\n–°–¢–ê–†–¢!!!')
    good_num = 0  # –û–±–Ω—É–ª–µ–Ω–∏–µ —Å—á–µ—Ç—á–∏–∫–∞ –ø—Ä–∞–≤–∏–ª—å–Ω—ã—Ö –æ—Ç–≤–µ—Ç–æ–≤
    all_num = len(people)  # –û–±—â–µ–µ —á–∏—Å–ª–æ –≤–æ–ø—Ä–æ—Å–æ–≤

    for key, val in people.items():  # –ü–µ—Ä–µ–±–æ—Ä –ø–∞—Ä –°–ª–æ–≤–∞—Ä—è (–∫–ª—é—á –∏ –∑–Ω–∞—á–µ–Ω–∏–µ)
        year = int(input(f'üîò –Ω–∞–∑–æ–≤–∏—Ç–µ –≥–æ–¥ —Ä–æ–∂–¥–µ–Ω–∏—è {key}: '))  # –ü—Ä–∏—Å–≤–æ–µ–Ω–∏–µ –≤–≤–µ–¥–µ–Ω–Ω–æ–≥–æ –≥–æ–¥–∞ –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é year
        if year == val:  # –°—Ä–∞–≤–Ω–µ–Ω–∏–µ –≤–≤–µ–¥–µ–Ω–Ω–æ–≥–æ –≥–æ–¥–∞ —Å–æ –∑–Ω–∞—á–µ–Ω–∏–µ–º –≤ –°–ª–æ–≤–∞—Ä–µ
            good_num += 1  # –ü—Ä–∏ —É–≥–∞–¥—ã–≤–∞–Ω–∏–∏ —É–≤–µ–ª–∏—á–µ–Ω–∏–µ —Å—á–µ—Ç—á–∏–∫–∞ –ø—Ä–∞–≤–∏–ª—å–Ω—ã—Ö –æ—Ç–≤–µ—Ç–æ–≤

    mist = all_num - good_num  # –í—ã—á–∏—Å–ª–µ–Ω–∏–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –æ—à–∏–±–æ–∫
    # –í—ã–≤–æ–¥ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –∏ %% –ø—Ä–∞–≤–∏–ª—å–Ω—ã—Ö –æ—Ç–≤–µ—Ç–æ–≤
    print(f'\n[+]‚úÖ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø—Ä–∞–≤–∏–ª—å–Ω—ã—Ö –æ—Ç–≤–µ—Ç–æ–≤: {good_num} [{round((good_num * 100) / all_num, 0)} %] –∏–∑ {all_num}')
    # –í—ã–≤–æ–¥ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –∏ %% –æ—à–∏–±–æ–∫
    print(f'[-]‚ùå –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –æ—à–∏–±–æ–∫: {mist} [{round((mist * 100) / all_num, 0)} %] –∏–∑ {all_num}')
    answer = input('\n–ù–µ –∂–µ–ª–∞–µ—Ç–µ –Ω–∞—á–∞—Ç—å –∏–≥—Ä—É —Å–Ω–∞—á–∞–ª–∞? [–¥–∞][–Ω–µ—Ç]: ')  # –ó–∞–ø—Ä–æ—Å –Ω–∞ –ø–æ–≤—Ç–æ—Ä–Ω—É—é –∏–≥—Ä—É
    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –æ—Ç–≤–µ—Ç–∞ (–≤–æ–∑–º–æ–∂–Ω—ã —Ä—É—Å. –∏ –∞–Ω–≥–ª. —è–∑. –≤ –ª—é–±–æ–º –Ω–∞–ø–∏—Å–∞–Ω–∏–∏ —Å—Ç—Ä–æ—á–Ω—ã–º–∏ –∏–ª–∏ –∑–∞–≥–ª–∞–≤–Ω—ã–º–∏ –±—É–∫–≤–∞–º–∏)
    if answer.lower() == '–¥–∞' or answer.lower() == 'yes':    # –ï—Å–ª–∏ '–¥–∞'/'yes', —Ç–æ –ø—Ä–æ–¥–æ–ª–∂–∏—Ç—å –∏–≥—Ä—É
        yes_no = True
    else:    # –í –∏–Ω—ã—Ö —Å–ª—É—á–∞—è—Ö - –æ—Å—Ç–∞–Ω–æ–≤
        yes_no = False
    return yes_no


print('\n‚ôªÔ∏è –ù–∞—á–∏–Ω–∞–µ–º –≤–∏–∫—Ç–æ—Ä–∏–Ω—É.\n–ó–∞–¥–∞—á–∞: –ø—Ä–∞–≤–∏–ª—å–Ω–æ –≤–≤–æ–¥–∏—Ç—å –≥–æ–¥ —Ä–æ–∂–¥–µ–Ω–∏—è –∑–Ω–∞–º–µ–Ω–∏—Ç–æ—Å—Ç–∏!\n')
while yes_no:  # –ë–µ—Å–∫–æ–Ω–µ—á–Ω—ã–π —Ü–∏–∫–ª, –ø–æ–∫–∞ –±—É–¥–µ—Ç –≤–≤–æ–¥–∏—Ç—Å—è '–¥–∞'/'yes' –Ω–∞ –ø–æ–≤—Ç–æ—Ä –∏–≥—Ä—ã
    yes_no = vic_game()  # –í—ã–∑–æ–≤ –§—É–Ω–∫—Ü–∏–∏, –∫–æ—Ç–æ—Ä–∞—è –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –ª–æ–≥–∏—á–µ—Å–∫–∏–π —Ç–∏–ø
